public class BuildInfo {

  private long build_num;

  private long job_id;

  public long getBuild_num() {
    return build_num;
  }

  public void setBuild_num(long build_num) {
    this.build_num = build_num;
  }

  public long getJob_id() {
    return job_id;
  }

  public void setJob_id(long job_id) {
    this.job_id = job_id;
  }
}
